     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $collection = Tweet::orderBy('created_at', 'desc')->paginate(10);

        $collection->transform(function ($tweet) {
            $attributes = $tweet->getAttributes();
            $attributes['user'] = $tweet->user()->first();
            $attributes['avatar'] = $tweet->user()->first()->avatar();

            return $attributes;
        });

        return $collection;
    }

